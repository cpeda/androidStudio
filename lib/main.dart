import 'package:flutter/material.dart';

void main() {
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      title: 'Ejemplo Trabajo N°1',
      debugShowCheckedModeBanner: false,
      home: Pantalla1(),
    );
  }
}

class Pantalla1 extends StatefulWidget {
  @override
  _Pantalla1State createState() => _Pantalla1State();
}

class _Pantalla1State extends State<Pantalla1> {
  final TextEditingController _num1Controller = TextEditingController();
  final TextEditingController _num2Controller = TextEditingController();

  String resultado = '';
  String error = '';

  void _sumar() {
    setState(() {
      error = '';
      final n1 = int.tryParse(_num1Controller.text);
      final n2 = int.tryParse(_num2Controller.text);
      if (n1 != null && n2 != null) {
        resultado = (n1 + n2).toString();
      } else {
        resultado = '';
        error = 'Error: No suma o multiplica';
      }
    });
  }

  void _multiplicar() {
    setState(() {
      error = '';
      final n1 = int.tryParse(_num1Controller.text);
      final n2 = int.tryParse(_num2Controller.text);
      if (n1 != null && n2 != null) {
        resultado = (n1 * n2).toString();
      } else {
        resultado = '';
        error = 'Error: No suma o multiplica';
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      backgroundColor: Colors.white,
      body: SafeArea(
        child: Center(
          child: SingleChildScrollView(
            padding: EdgeInsets.all(20),
            child: Column(
              children: [
                Icon(Icons.cloud, size: 80, color: Colors.grey),
                SizedBox(height: 10),
                Text("Ejemplo Trabajo N°1",
                    style: TextStyle(color: Colors.grey)),
                SizedBox(height: 10),
                Text("Calcular el Resultado",
                    style: TextStyle(
                        fontSize: 22, fontWeight: FontWeight.bold)),
                SizedBox(height: 30),
                TextField(
                  controller: _num1Controller,
                  keyboardType: TextInputType.number,
                  decoration: InputDecoration(
                    labelText: 'Número N°1',
                    border: OutlineInputBorder(),
                  ),
                ),
                SizedBox(height: 20),
                TextField(
                  controller: _num2Controller,
                  keyboardType: TextInputType.number,
                  decoration: InputDecoration(
                    labelText: 'Número N°2',
                    border: OutlineInputBorder(),
                  ),
                ),
                SizedBox(height: 20),
                Text('Resultado: $resultado',
                    style: TextStyle(fontSize: 16)),
                SizedBox(height: 20),
                Row(
                  mainAxisAlignment: MainAxisAlignment.center,
                  children: [
                    ElevatedButton(
                      onPressed: _sumar,
                      child: Text('Sumar'),
                    ),
                    SizedBox(width: 20),
                    ElevatedButton(
                      onPressed: _multiplicar,
                      style: ElevatedButton.styleFrom(
                          backgroundColor: Colors.indigo),
                      child: Text('Multiplicar'),
                    ),
                  ],
                ),
                SizedBox(height: 20),
                if (error.isNotEmpty)
                  Text(
                    error,
                    style: TextStyle(color: Colors.red),
                  ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}





























































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































